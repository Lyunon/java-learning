package sec01.exam01;

public class VariableInitialization {
  public static void main(String[] args){
    //value라는 변수 선언함.
    int value;

    //value라는 변수 값을 읽고 10을 더하는 산술 연산 수행, 연산의 결과값을 result라는 변수의 초기값으로 저장함.
    int result = value + 10;

    //result라는 변수 값을 읽고 콘솔에 출력함.
    System.out.println(result);
  }
}
